package chap04.exam04;

public class WhileExam1 {
    public static void main(String[] args) {
        for(int i=0; i<5; i++){
            System.out.println("i : "+i);
        }
        System.out.println();

        int i = 0;
        while(i<5){
            System.out.println("i : "+i);
            i++;
        }
    }
}
