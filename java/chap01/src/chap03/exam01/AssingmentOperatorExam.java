package chap03.exam01;

public class AssingmentOperatorExam {
    public static void main(String[] args) {
        int n1 = 0;
        n1 = n1 + 5;
        System.out.println(n1);
        int n2 = 0;
        n2 += 5;
        System.out.println(n2);

        int n3 = 10;
        n3 -= 4;
        System.out.println(n3);
    }
}
